#include "mathft.h"

int	ft_is_prime(size_t n)
{
	size_t	i;

	if (n < 2 || n % 2 == 0)
		return (0);
	if (n == 2 || n == 3)
		return (1);
	i = 3;
	while (i * i <= n)
	{
		if (n % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
